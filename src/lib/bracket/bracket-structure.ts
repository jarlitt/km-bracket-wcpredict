import type { KnockoutMatch, TeamStanding } from '@/types';

interface BracketSlot {
  position: 1 | 2 | 3;
  groupId: string;
}

function slotLabel(slot: BracketSlot): string {
  return `${slot.position === 1 ? '1' : slot.position === 2 ? '2' : '3'}${slot.groupId}`;
}

function resolveTeamId(
  slot: BracketSlot,
  standings: Record<string, TeamStanding[]>,
): number | null {
  const groupStandings = standings[slot.groupId];
  if (!groupStandings || groupStandings.length < slot.position) return null;
  return groupStandings[slot.position - 1].team.id;
}

